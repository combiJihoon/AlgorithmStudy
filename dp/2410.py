import sys

input = sys.stdin.readline
n = int(input())
dp = [0] * 1000001
dp[1] = 1
dp[2] = 2
for i in range(3, n+1):
    if i % 2 != 0:
        dp[i] = dp[i-1] % int(1e9)
    else:
        dp[i] = (dp[i-1] + dp[i//2]) % int(1e9)
# print(dp)
print(dp[n])

